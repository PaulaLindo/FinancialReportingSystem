    def apply_fund_tags(self, trial_balance_data: pd.DataFrame) -> pd.DataFrame:
        """Apply fund tags to trial balance data"""
        tagged_data = trial_balance_data.copy()
        
        # Initialize fund tag columns
        tagged_data['fund_code'] = ''
        tagged_data['fund_name'] = ''
        tagged_data['fund_restriction'] = ''
        tagged_data['fund_reporting_required'] = False
        
        # Apply fund tags based on account codes and descriptions
        for index, row in tagged_data.iterrows():
            account_code = str(row.get('Account Code', ''))
            account_desc = str(row.get('Account Description', '')).lower()
            
            fund_tag = self._determine_fund_tag(account_code, account_desc)
            tagged_data.at[index, 'fund_code'] = fund_tag['code']
            tagged_data.at[index, 'fund_name'] = fund_tag['name']
            tagged_data.at[index, 'fund_restriction'] = fund_tag['restrictions']
            tagged_data.at[index, 'fund_reporting_required'] = fund_tag['reporting_required']
        
        return tagged_data
    
    def _determine_fund_tag(self, account_code: str, account_desc: str) -> Dict[str, str]:
        """Determine fund tag based on account code and description"""
        account_code = account_code.zfill(4)
        
        # Government funds - typically revenue from government sources
        if account_code.startswith('4') and any(keyword in account_desc for keyword in ['government', 'grant', 'subsidy', 'tax']):
            return self.fund_structure['government_funds']
        
        # Donor funds - typically restricted revenue
        elif account_code.startswith('4') and any(keyword in account_desc for keyword in ['donor', 'funding', 'aid', 'sponsor']):
            return self.fund_structure['donor_funds']
        
        # Own revenue - general revenue generation
        elif account_code.startswith('4'):
            return self.fund_structure['own_revenue_funds']
        
        # Capital funds - asset purchases and capital expenditure
        elif account_code.startswith('1') and any(keyword in account_desc for keyword in ['capital', 'asset', 'equipment', 'investment']):
            return self.fund_structure['capital_funds']
        
        # Trading funds - operational income
        elif account_code.startswith('4') and any(keyword in account_desc for keyword in ['trading', 'sales', 'commercial']):
            return self.fund_structure['trading_funds']
        
        # Contingency funds - reserves and provisions
        elif account_code.startswith('2') and any(keyword in account_desc for keyword in ['contingency', 'reserve', 'provision', 'emergency']):
            return self.fund_structure['contingency_funds']
        
        # Default to own revenue for unspecified
        else:
            return self.fund_structure['own_revenue_funds']
    
