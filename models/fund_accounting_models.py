    def _analyze_cross_segments(self, tagged_data: pd.DataFrame) -> Dict[str, Any]:
        """Analyze cross-segment relationships"""
        cross_analysis = {
            'fund_by_department': {},
            'function_by_fund': {},
            'department_by_function': {},
            'top_combinations': []
        }
        
        # Fund by Department
        fund_dept_pivot = tagged_data.groupby(['fund_code', 'department_code'])['Net Balance'].sum().abs()
        for (fund_code, dept_code), amount in fund_dept_pivot.items():
            if amount > 0:
                if fund_code not in cross_analysis['fund_by_department']:
                    cross_analysis['fund_by_department'][fund_code] = {}
                cross_analysis['fund_by_department'][fund_code][dept_code] = amount
        
        # Function by Fund
        func_fund_pivot = tagged_data.groupby(['function_code', 'fund_code'])['Net Balance'].sum().abs()
        for (func_code, fund_code), amount in func_fund_pivot.items():
            if amount > 0:
                if func_code not in cross_analysis['function_by_fund']:
                    cross_analysis['function_by_fund'][func_code] = {}
                cross_analysis['function_by_fund'][func_code][fund_code] = amount
        
        # Department by Function
        dept_func_pivot = tagged_data.groupby(['department_code', 'function_code'])['Net Balance'].sum().abs()
        for (dept_code, func_code), amount in dept_func_pivot.items():
            if amount > 0:
                if dept_code not in cross_analysis['department_by_function']:
                    cross_analysis['department_by_function'][dept_code] = {}
                cross_analysis['department_by_function'][dept_code][func_code] = amount
        
        # Top combinations
        all_combinations = []
        for (fund_code, dept_code), amount in fund_dept_pivot.items():
            if amount > 0:
                all_combinations.append({
                    'combination': f"{fund_code}-{dept_code}",
                    'amount': amount,
                    'fund_name': self.fund_structure.get(fund_code, {}).get('name', 'Unknown'),
                    'department_name': self.department_structure.get(dept_code, {}).get('name', 'Unknown')
                })
        
        # Sort by amount and get top 10
        cross_analysis['top_combinations'] = sorted(all_combinations, key=lambda x: x['amount'], reverse=True)[:10]
        
        return cross_analysis
    
    def _calculate_segment_compliance(self, tagged_data: pd.DataFrame) -> Dict[str, Any]:
        """Calculate segment reporting compliance metrics"""
        total_records = len(tagged_data)
        tagged_records = len(tagged_data[
            (tagged_data['fund_code'] != '') & 
            (tagged_data['department_code'] != '') & 
            (tagged_data['function_code'] != '')
        ])
        
        compliance_metrics = {
            'total_records': total_records,
            'tagged_records': tagged_records,
            'tagging_coverage': (tagged_records / total_records) * 100 if total_records > 0 else 0,
            'fund_types_covered': len(tagged_data['fund_code'].unique()),
            'department_types_covered': len(tagged_data['department_code'].unique()),
            'function_types_covered': len(tagged_data['function_code'].unique()),
            'compliance_score': 0.0,
            'risk_level': 'LOW'
        }
        
        # Calculate compliance score
        if compliance_metrics['tagging_coverage'] >= 95:
            compliance_metrics['compliance_score'] = 100.0
            compliance_metrics['risk_level'] = 'LOW'
        elif compliance_metrics['tagging_coverage'] >= 80:
            compliance_metrics['compliance_score'] = 85.0
            compliance_metrics['risk_level'] = 'MEDIUM'
        else:
            compliance_metrics['compliance_score'] = 70.0
            compliance_metrics['risk_level'] = 'HIGH'
        
        return compliance_metrics
